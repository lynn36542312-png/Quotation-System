export const groupedByPdm = Object.entries(
  brandDirectory.reduce<Record<string, BrandDirectoryRow[]>>((acc, row) => {
    if (!acc[row.pdm]) acc[row.pdm] = [];
    acc[row.pdm].push(row);
    return acc;
  }, {})
)
  .map(([pmName, brands]) => ({
    pmName,
    brands: brands.sort((a, b) => a.brand.localeCompare(b.brand, 'zh-Hant')),
  }))
  .sort((a, b) => a.pmName.localeCompare(b.pmName, 'zh-Hant'));

export function findBrand(brandName: string) {
  return brandDirectory.find((row) => row.brand.toLowerCase() === decodeURIComponent(brandName).toLowerCase());
}
